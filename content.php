                        <p class="justify">Pendidikan adalah pembelajaran pengetahuan, keterampilan, dan kebiasaan sekelompok orang yang diturunkan dari satu generasi ke generasi berikutnya melalui pengajaran, pelatihan, atau penelitian. Pendidikan sering terjadi di bawah bimbingan orang lain, tetapi juga memungkinkan secara otodidak. Etimologi kata pendidikan itu sendiri berasal dari bahasa Latin yaitu ducare, berarti “menuntun, mengarahkan, atau memimpin” dan awalan e, berarti “keluar”. Jadi, pendidikan berarti kegiatan “menuntun ke luar”. Setiap pengalaman yang memiliki efek formatif pada cara orang berpikir, merasa, atau tindakan dapat dianggap pendidikan. Pendidikan umumnya dibagi menjadi tahap seperti prasekolah, sekolah dasar, sekolah menengah pertama, sekolah menengah atas, dan kemudian perguruan tinggi.
